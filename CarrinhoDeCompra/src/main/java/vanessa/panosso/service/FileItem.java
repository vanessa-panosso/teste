package vanessa.panosso.service;

public class FileItem {
	private String name;
	
	private byte[] foto;


	public String getName() {
		return name;
	}

	public void setName(String name) {
		this.name = name;
	}

	public byte[] getFoto() {
		return foto;
	}

	public void setFoto(byte[] foto) {
		this.foto = foto;
	}
}
